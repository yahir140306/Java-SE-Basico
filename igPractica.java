public class igPractica {
    
    public static void main (String[] args) {
        
        char ch = 'A';
        for (int i=1; i <= 3; i++) {
            for (int j=1; j <= 3; j++) {
                System.out.println(ch);
                ch++;
            }
            System.out.println();
        }
    }
}